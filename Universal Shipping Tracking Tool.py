        async def push_citation(
            document_chunks: List[str],
            source_name: str,
            source_url: str,
            extra_meta: Optional[Dict[str, str]] = None,
        ):
            """推送到 Reference（Citations）侧边栏"""
            if __event_emitter__ is None:
                return
            meta = [
                {"date_accessed": datetime.now().isoformat(), "source": source_name}
            ]
            if extra_meta:
                # 把简单的键值对塞进 metadata 方便检索
                for k, v in extra_meta.items():
                    meta.append({k: str(v)})
            await __event_emitter__(
                {
                    "type": "citation",
                    "data": {
                        "document": document_chunks,
                        "metadata": meta,
                        "source": {"name": source_name, "url": source_url},
                    },
                }
            )

        # =============== 参数与校验 ===============
        if show_reference is None:
            show_reference = bool(self.valves.show_reference)

        if not company or not number:
            msg = [
                ":warning: **请提供完整参数**",
                "- company：快递公司名称或别名",
                "- number：快递单号",
            ]
            # 不推 citation，避免把报错也塞进 Reference
            if show_reference:
                msg.append("\n" + reference_output())
            return "\n".join(msg)

        com_code, suggestions = find_express_company(company)
        if not com_code:
            tip = "、".join(suggestions) if suggestions else "无匹配建议"
            return f"❌ 未识别的快递公司：**{company}**。可尝试：{tip}"

        # =============== 组装请求参数 ===============
        param_obj = {
            "com": com_code,
            "num": number,
            "phone": phone or "",
            "from": from_addr or "",
            "to": to_addr or "",
            "resultv2": resultv2,
            "show": 0,
            "order": order,
            "page": page,
            "limit": limit,
        }
        param_json = json.dumps(param_obj, ensure_ascii=False)

        # =============== 无密钥时返回参考示例/提示 ===============
        if not self.valves.customer_code or not self.valves.api_key:
            hint = [
                "⚠️ 未配置 `customer_code` 与 `api_key`，已返回参考输出示例。",
                "前往快递100企业版获取授权后，在 Valves 中填写即可实时查询。",
            ]
            if show_reference:
                example_md = reference_output()
                hint.append("\n" + example_md)
                # 也把示例推到 Reference 里，便于点击查看
                await push_citation(
                    [example_md],
                    source_name="示例输出（快递查询工具）",
                    source_url="https://docs.openwebui.com/features/plugin/tools/development/",
                    extra_meta={"note": "示例展示，仅供参考"},
                )
            else:
                hint.append("(可将 `show_reference` 设为 true 查看格式示例)")
            return "\n".join(hint)

        # =============== 签名与请求 ===============
        sign_src = (
            param_json + self.valves.api_key + self.valves.customer_code
        ).encode("utf-8")
        sign = hashlib.md5(sign_src).hexdigest().upper()
        payload = {
            "customer": self.valves.customer_code,
            "sign": sign,
            "param": param_json,
        }

        try:
            resp = requests.post(
                self.valves.api_url,
                data=payload,
                timeout=self.valves.timeout_seconds,
            )
            resp.raise_for_status()
            data = resp.json()
        except requests.exceptions.RequestException as e:
            return f"❌ 网络请求失败：{e}"
        except ValueError:
            return "❌ 返回内容不是合法的 JSON"

        if raw:
            # raw 模式不推 citation，保持“只返回原始 JSON”的语义
            return data

        status = str(data.get("status", ""))
        message = str(data.get("message", ""))
        state = str(data.get("state", ""))
        ischeck = str(data.get("ischeck", ""))
        nu = str(data.get("nu", number))
        com = str(data.get("com", com_code))
        last_result_ok = status == "200"

        if not last_result_ok:
            human = err_desc(status)
            return f"❌ 查询失败（status={status}）：{message or human}"

        # =============== 格式化输出 ===============
        signed = "已签收" if ischeck == "1" or state == "3" else "未签收"
        state_h = f"{state_icon(state)} **{state_desc(state)}**"

