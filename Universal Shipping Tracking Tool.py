        predicted_route = data.get("predictedRoute") or []
        lines.append(format_route_info(route_info))
        lines.append(format_predicted(predicted_route))

        # 轨迹
        traces = data.get("data") or []
        lines.append("\n## 🧭 物流轨迹（最近10条）\n")
        lines.append(format_tracking(traces, max_items=10))

        # 备注
        query_time = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
        lines.append("\n---")
        lines.append(f"* 查询时间：{query_time}*\n")

        rendered_md = "\n".join([s for s in lines if s])

        rendered_md = "\n".join([s for s in lines if s])

        # =============== 推送到 Reference（Citation） ===============
        # 直接把完整的已格式化 Markdown 发到 Reference 面板
        await push_citation(
            document_chunks=[rendered_md],
            source_name="快递100 API",
            source_url="https://www.kuaidi100.com/",
            extra_meta={
                "company": com,
                "tracking_number": nu,
                "state": state_desc(state),
                "checked": ("已签收" if signed == "已签收" else "未签收"),
            },
        )

        return rendered_md
